export function getConstellationBySlug(slug: string): Constellation | undefined {
  return constellations.find(c => c.slug === slug);
}

export function getConstellationById(id: string): Constellation | undefined {
  return constellations.find(c => c.id === id);
}

export function getConstellationsByFilter(season?: string, hemisphere?: string): Constellation[] {
  return constellations.filter(c => {
    if (season && c.bestSeason.toLowerCase() !== season.toLowerCase()) return false;
    if (hemisphere && c.hemisphere !== hemisphere && c.hemisphere !== 'both') return false;
    return true;
  });
}

export function searchConstellations(query: string): Constellation[] {
  const q = query.toLowerCase();
  return constellations.filter(c =>
    c.name.toLowerCase().includes(q) ||
    c.alternateNames.some(n => n.toLowerCase().includes(q)) ||
    c.abbreviation.toLowerCase().includes(q) ||
    c.stars.some(s => s.name.toLowerCase().includes(q))
  );
}
